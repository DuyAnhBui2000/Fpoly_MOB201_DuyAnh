package buiduyanh.fpoly.test_lab3_2;

public class BookmarkItem {
        private String url;
        private String title;

        public BookmarkItem(String url, String title) {
            this.url = url;
            this.title = title;
        }

        public String getUrl() {
            return url;
        }

        public String getTitle() {
            return title;
        }
    }
